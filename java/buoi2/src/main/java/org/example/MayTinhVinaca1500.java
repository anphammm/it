package org.example;

public class MayTinhVinaca1500 implements MayTinhBoTuiInterface {
    public MayTinhVinaca1500() {
    }

    public double cong(double a, double b) {
        return a + b;
    }

    public double tru(double a, double b) {
        return a - b;
    }

    public double nhan(double a, double b) {
        return a * b;
    }

    public double chia(double a, double b) {
        return a / b;
    }
}
